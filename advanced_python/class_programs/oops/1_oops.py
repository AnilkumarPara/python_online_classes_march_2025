class Employee:
    def __init__(self, f_name, l_name, eid):
        self.f_name = f_name
        self.l_name = l_name
        self.eid = eid

    def display(ss):
        print(ss.f_name)
        print(ss.l_name)
        print(ss.eid)

emp1 = Employee('Anil', 'Para', 389)
print(emp1)
emp2 = Employee('Sachin', 'Tendulakar', 400)
print(emp2)

emp1.display()
emp2.display()

